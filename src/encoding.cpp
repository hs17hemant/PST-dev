// pstwriter/encoding.cpp
//
// Block encoding for PST data blocks ([MS-PST] §5.1, §5.2).
//
// The spec defines a SINGLE 768-byte array `mpbbCrypt[]` that is sliced
// into three logical 256-byte sub-tables:
//
//     mpbbR = mpbbCrypt + 0     §5.1 ENCRYPT       (also used by §5.2)
//     mpbbS = mpbbCrypt + 256   §5.2 mixing only
//     mpbbI = mpbbCrypt + 512   §5.1 DECRYPT (= inverse of mpbbR)
//
// Pstwriter is write-only, so the §5.1 path only applies mpbbR.  The §5.2
// cyclic cipher needs all three tables; per the spec note in §5.2, the
// cipher is symmetric — calling it twice with the same dwKey returns the
// original buffer.
//
// Table provenance: the 768 bytes below were assembled from the
// libyal/libpff source `libpff_encryption.c` (the most widely-deployed
// open-source PST reader).  libpff's table names map to the spec names as:
//     libpff_encryption_high1[]        ↔  mpbbR  ([MS-PST] §5.1 encrypt)
//     libpff_encryption_high2[]        ↔  mpbbS  ([MS-PST] §5.2 mix)
//     libpff_encryption_compressible[] ↔  mpbbI  ([MS-PST] §5.1 decrypt)
// The first-8-bytes landmarks below match the values listed in the
// project's HANDOFF brief, which were independently verified against the
// spec source.  Compile-time assertions at the bottom of the table block
// catch any single-byte typo before the test suite runs.

