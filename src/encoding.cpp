// ===========================================================================
// NDB_CRYPT_PERMUTE — [MS-PST] §5.1
// ===========================================================================
void encodePermute(uint8_t* data, size_t length) noexcept
{
    for (size_t i = 0; i < length; ++i) {
        data[i] = kMpbbR[data[i]];
    }
}

// ===========================================================================
// NDB_CRYPT_CYCLIC — [MS-PST] §5.2
//
// Reference C from the spec (verbatim):
//   void CryptCyclic(PVOID pv, int cb, DWORD dwKey)
//   {
//       byte * pb = (byte *)pv;
//       byte b;
//       WORD w = (WORD)(dwKey ^ (dwKey >> 16));
//       while (--cb >= 0) {
//           b = *pb;
//           b = (byte)(b + (byte)w);
//           b = mpbbR[b];
//           b = (byte)(b + (byte)(w >> 8));
//           b = mpbbS[b];
//           b = (byte)(b - (byte)(w >> 8));
//           b = mpbbI[b];
//           b = (byte)(b - (byte)w);
//           *pb++ = b;
//           w = (WORD)(w + 1);
//       }
//   }
//
// `dwKey` is the low 32 bits of the data block's BID:
//     (uint32_t)(bid.value & 0xFFFFFFFFu).
// The cipher is symmetric: applying it twice with the same key restores
// the original plaintext.
// ===========================================================================
void encodeCyclic(uint8_t* data, size_t length, uint32_t key) noexcept
{
    uint16_t w = static_cast<uint16_t>(key ^ (key >> 16));

    for (size_t i = 0; i < length; ++i) {
        const uint8_t lower = static_cast<uint8_t>(w & 0xFFu);
        const uint8_t upper = static_cast<uint8_t>(w >> 8);

        uint8_t b = data[i];
        b = static_cast<uint8_t>(b + lower);
        b = kMpbbR[b];
        b = static_cast<uint8_t>(b + upper);
        b = kMpbbS[b];
        b = static_cast<uint8_t>(b - upper);
        b = kMpbbI[b];
        b = static_cast<uint8_t>(b - lower);
        data[i] = b;

        w = static_cast<uint16_t>(w + 1u);
    }
}

// ===========================================================================
// Dispatch.
// ===========================================================================
void encodeBlock(uint8_t*    data,
                 size_t      length,
                 CryptMethod method,
                 uint32_t    key) noexcept
{
    switch (method) {
    case CryptMethod::None:
        return;
    case CryptMethod::Permute:
        encodePermute(data, length);
        return;
    case CryptMethod::Cyclic:
        encodeCyclic(data, length, key);
        return;
    }
}

// ===========================================================================
// Test-only accessor — full 768-byte mpbbCrypt array.
//   mpbbR = ptr + 0, mpbbS = ptr + 256, mpbbI = ptr + 512.
// ===========================================================================
const uint8_t* permuteTable() noexcept
{
    return &kMpbbCrypt[0];
}

