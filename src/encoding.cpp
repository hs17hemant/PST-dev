// Sub-table base addresses, exactly as the §5.1 #defines lay them out.
constexpr const uint8_t* kMpbbR = &kMpbbCrypt[0];     // §5.1 encrypt
constexpr const uint8_t* kMpbbS = &kMpbbCrypt[256];   // §5.2 mix
constexpr const uint8_t* kMpbbI = &kMpbbCrypt[512];   // §5.1 decrypt

// ---------------------------------------------------------------------------
// Compile-time table integrity checks.
//
//   1. Every sub-table is a permutation (each byte 0..255 appears once).
//   2. mpbbI is the mathematical inverse of mpbbR.
//   3. mpbbS is involutory (cyclic algorithm depends on this property).
//   4. Spec-named landmark bytes match HANDOFF.md.
// ---------------------------------------------------------------------------
constexpr bool isPermutation(const uint8_t* t) noexcept
{
    bool seen[256] = {};
    for (size_t i = 0; i < 256; ++i) {
        if (seen[t[i]]) {
            return false;
        }
        seen[t[i]] = true;
    }
    return true;
}

constexpr bool isInverseOf(const uint8_t* a, const uint8_t* b) noexcept
{
    for (size_t i = 0; i < 256; ++i) {
        if (b[a[i]] != static_cast<uint8_t>(i)) {
            return false;
        }
    }
    return true;
}

constexpr bool isInvolution(const uint8_t* t) noexcept
{
    for (size_t i = 0; i < 256; ++i) {
        if (t[t[i]] != static_cast<uint8_t>(i)) {
            return false;
        }
    }
    return true;
}

static_assert(isPermutation(kMpbbR), "mpbbR is not a permutation");
static_assert(isPermutation(kMpbbS), "mpbbS is not a permutation");
static_assert(isPermutation(kMpbbI), "mpbbI is not a permutation");
static_assert(isInverseOf(kMpbbR, kMpbbI),
              "mpbbI must be the inverse of mpbbR ([MS-PST] §5.1)");
static_assert(isInvolution(kMpbbS),
              "mpbbS must be involutory ([MS-PST] §5.2 cyclic depends on it)");

// HANDOFF.md spec-verified landmarks.
static_assert(kMpbbR[0] == 65  && kMpbbR[7] == 187, "mpbbR landmark mismatch");
static_assert(kMpbbS[0] == 20  && kMpbbS[7] == 156, "mpbbS landmark mismatch");
static_assert(kMpbbI[0] == 71  && kMpbbI[7] == 72,  "mpbbI landmark mismatch");
static_assert(kMpbbI[255] == 236, "mpbbI[255] landmark mismatch");

